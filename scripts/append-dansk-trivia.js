async function main() {
  const existing = JSON.parse(readFileSync(PATH, 'utf-8'));
  // (trivia file uses flat array, nothing to adapt)
  const existingTexts = new Set(existing.map(q => q.questionText));

  let added = 0;
  for (const q of NEW_QUESTIONS) {
    if (existingTexts.has(q.questionText)) continue;
    existing.push({
      ...q,
      id: crypto.randomBytes(4).toString('hex'),
      validated: true,
      timesUsed: 0,
      createdAt: new Date().toISOString(),
      locale: 'dk',
      source: 'opus-curated',
    });
    added++;
  }

  writeFileSync(PATH, JSON.stringify(existing, null, 2));
  console.log(`✅ Added ${added} questions. Total: ${existing.length}`);

  // Type distribution
  const types = {};
  for (const q of existing) {
    types[q.questionType] = (types[q.questionType] || 0) + 1;
  }
  console.log('\nType distribution:');
  for (const [type, count] of Object.entries(types)) {
    console.log(`  ${type}: ${count}`);
  }
}

main().catch(err => { console.error('FATAL:', err); process.exit(1); });
